## How much revenue is generated by Repeat Customers vs One-time Customers? \\\ And Repeat vs One-time × Avg Order Value
with Orders as (
			select 
				CustomerID,
                sum(Revenue) As Total_Revenue,
                count(distinct invoice) as Total_Orders,
                case 
                   when count(distinct invoice) > 1 then 'Repeat_Customers'
                   else 'One_Time_Customers'
				End As Customer_Type
			From Fact_Orders
            where CustomerId <> 0
            group by CustomerID )

select
	Customer_Type,
    round( Count(CustomerID) / (select Count(distinct CustomerID) from Fact_Orders where CustomerId <> 0) *100 ,2) As Percentage_Of_Customers,
    sum(Total_Revenue) As Total_Revenue,
    round( Sum(Total_Revenue) / (select Sum(Revenue) from Fact_Orders where CustomerID <> 0 ) *100 ,2) as Revenue_Share_Percentage,
    sum(Total_Orders)  As Total_Orders,
    round( sum(Total_Revenue) / sum(Total_Orders)  ,2) As AOV
from Orders
group by Customer_Type ;


