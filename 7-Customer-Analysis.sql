# Who is New and lost RFM, even though he's a Repeat Customer?  

with Orders As (
			select 
               CustomerID,
               Sum(Revenue) As Total_Revenue,
               Count(distinct invoice) as Total_Orders,
               case 
                  when Count(distinct Invoice) > 1 then 'Repeat_Customer'
                  else 'One_time_Customer'
			   End As Customer_Type
			From Fact_Orders
            Where CustomerID <> 0
            group by CustomerID )

select 
    RFM.CustomerID,
    RFM.RFM_Segment,
    O.Customer_Type,
	sum(O.Total_Revenue) as Total_Revenue,
    sum(O.Total_Orders) as Total_Orders ,
    round( sum(O.Total_Revenue) / sum(O.Total_Orders) ,2) As AOV
from RFM_CLV_Customer RFM
join Orders O              On RFM.CustomerID = O.CustomerID
where RFM.CustomerID <> 0 And RFM.RFM_Segment in ( 'New', 'Lost' ) And  O.Customer_Type = 'Repeat_Customer'
group by RFM.CustomerID ;



