# Which Segment has Lowest Returns?

with Orders as (
    select CustomerID, sum(Revenue) as Total_Revenue
    from fact_orders
    group by CustomerID
),
Returns_ as (
    select CustomerID, sum(Total_Amount) as Total_Returns
    from fact_returns
    group by CustomerID
)

select 
   A.RFM_Segment,
   Sum( O.Total_Revenue ) as Total_Revenue ,
   Sum(coalesce( R.Total_Returns,0)) as Total_Returns ,
   round( Sum(coalesce( R.Total_Returns,0)) / Sum( O.Total_Revenue ) * 100 ,2) as Return_Rate 
from rfm_clv_customer A 
left join Orders O   on A.CustomerID = O.CustomerID
left join Returns_ R on A.CustomerID = R.CustomerID
where O.CustomerID <> 0
group by A.RFM_Segment ; 

 
